// Create an array of integers 1 through 5
var array1 = [1, 2, 3, 4, 5];
console.log(array1);

// Create an empty array using the Array constructor
var array2 = new Array();
console.log(array2);

// Create an array with a length of 2 but no elements
var array3 = new Array(2);
console.log(array3);

// Create an array with 3 elements using the Array constructor
var array4 = new Array(12, 3, 4);
console.log(array4);

// Create an array of mixed data types
var array5 = [10, "Amit", true];
console.log(array5);



// The code above demonstrates various ways to create 
// and initialize arrays in JavaScript.

// Arrays can hold elements of any data type, including numbers, strings, 
// and booleans.

// You can initialize an array with elements using square brackets [] 
// or the Array constructor.

// When creating an array with the Array constructor, 
//you can pass a single number to specify the array's length
